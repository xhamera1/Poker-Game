package pl.edu.agh.kis.pz1.model;

/**
 * Enum representing the suits of cards in a deck.
 * Each suit corresponds to a specific symbol used in a deck of cards.
 */
public enum Suit
{
    SPADE,      // Pik (♠) - czarna
    HEART,      // Serce (♥) - czerwona
    DIAMOND,    // Karo (♦) - czerwona
    CLUB,       // Zoladz (♣) - zielona

}
